vector< LL > divisor(LL n) {
  vector< LL > ret;
  for(LL i = 1; i * i <= n; i++) {
    if(n % i == 0) {
      ret.push_back(i);
      if(i * i != n) ret.push_back(n / i);
    }
  }
  sort(begin(ret), end(ret));
  return (ret);
}

int main() {
    LL N;cin >> N;
    auto v = divisor(N);

    LL ans = 0;
    for(auto t : v)ans += t;

    if(ans < N*2)cout << "Deficient" << endl;
    else if(ans == N*2)cout << "Perfect" << endl;
    else cout << "Abundant" << endl;
}

