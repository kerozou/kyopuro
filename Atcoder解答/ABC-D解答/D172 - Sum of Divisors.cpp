template <typename T>
struct PrimeFact {
    vector<T> spf;
    PrimeFact(T N) { init(N); }
    void init(T N) { // 前処理。spf を求める
        spf.assign(N + 1, 0);
        for (T i = 0; i <= N; i++) spf[i] = i;
        for (T i = 2; i * i <= N; i++) {
            if (spf[i] == i) {
                for (T j = i * i; j <= N; j += i) {
                    if (spf[j] == j) {
                        spf[j] = i;
                    }
                }
            }
        }
    }
    map<T, T> get(T n) { // nの素因数分解を求める
        map<T, T> m;
        while (n != 1) {
            m[spf[n]]++;
            n /= spf[n];
        }
        return m;
    }
};

int main() {
    int N;cin >> N;
    PrimeFact pr(N);
    
    LL ans = 0;
    for(int i = 1;i <= N;i++){
        map<int,int> m = pr.get(i);
        LL tmp = 1;
        for(auto [x,y] : m){
            tmp *= (y+1); 
        }
        ans += i*tmp;
    }

    cout << ans << endl;

}

